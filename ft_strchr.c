char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*tem;

	i = 0;
	tem = (char *)s;
	while (tem[i])
	{
		if (tem[i] == c)
			return (&tem[i]);
		i ++;
	}
	if (c == '\0')
		return (&tem[i]);
	return (0);
}
